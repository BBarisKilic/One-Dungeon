/// This class contains layout constants used throughout the game UI.
abstract final class GameLayout {
  /// Standard vertical space.
  static const verticalSpace = 16.0;

  /// Extended vertical space.
  static const longVerticalSpace = 32.0;

  /// Standard button padding.
  static const buttonPadding = 24.0;

  /// General width for UI.
  static const generalWidth = 400.0;

  /// General height for UI.
  static const generalHeight = 600.0;
}
